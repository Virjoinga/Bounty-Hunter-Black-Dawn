	public static string GetFlyAniString(MoveDirection dir)
	{
		string result = FlyForward;
		switch (dir)
		{
		case MoveDirection.forward:
			result = FlyForward;
			break;
		case MoveDirection.back:
			result = FlyBack;
			break;
		case MoveDirection.left:
			result = FlyLeft;
			break;
		case MoveDirection.right:
			result = FlyRight;
			break;
		}
		return result;
	}
